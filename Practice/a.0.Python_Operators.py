# Python Operators Overview

'''
Arithmetic operators: +, -, /, *, **, //, %
Comparison/Relational operators: >, <, >=, <=, ==, !=
Logical Operators: and, or, not
Assignment Operators: =, and shortcut operator for all +, -, /, *, **, //, %
Identity Operators: is
Membership Operators: in, not in
Bitwise Operators: <<, >>, |, ^, &, ~
Walrus Operator: :=
'''

# Arithmetic Operators
a = 10
b = 3

# Addition
addition = a + b
print("Addition :" , addition)

# Subtraction
subtraction = a - b
print("Subtraction:", subtraction)  # 7

# Multiplication
multiplication = a * b
print("Multiplication:", multiplication)  # 30

# Division (float)
division = a / b
print("Division:", division)  # 3.333...

# Floor Division (integer division)
floor_division = a // b
print("Floor Division:", floor_division)  # 3

# Modulus (remainder)
modulus = a % b
print("Modulus:", modulus)  # 1
